    public class InMemoryCarDal : ICarDal
    {
        List<Car> _cars;
        public InMemoryCarDal()
        {
            _cars = new List<Car> { 
                new Car { BrandId=1, ColorId=1, DailyPrice=100, ModelYear=2020, Description="Mercedes" },
                new Car { BrandId=2, ColorId=2, DailyPrice=10, ModelYear=1996, Description="Şahin" },
                new Car { BrandId=3, ColorId=2, DailyPrice=50, ModelYear=2003, Description="Citroen" },
                new Car { BrandId=4, ColorId=3, DailyPrice=110, ModelYear=2020, Description="Volswagen" }



            };
        }
        public void Add(Car car)
        {
            _cars.Add(car);
        }

        public void Delete(Car car)
        {
            Car carToDelete = _cars.SingleOrDefault(c => c.BrandId == car.BrandId);
            _cars.Remove(carToDelete);
        }

